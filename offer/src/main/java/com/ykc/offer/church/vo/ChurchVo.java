package com.ykc.offer.church.vo;

public class ChurchVo {
	
	private String ch_no = null;
	private String ch_id = null;
	private String ch_name = null;
	private String ch_addr = null;
	private String ch_telno = null;
	
	public String getCh_no() {
		return ch_no;
	}
	public void setCh_no(String ch_no) {
		this.ch_no = ch_no;
	}
	public String getCh_id() {
		return ch_id;
	}
	public void setCh_id(String ch_id) {
		this.ch_id = ch_id;
	}
	public String getCh_name() {
		return ch_name;
	}
	public void setCh_name(String ch_name) {
		this.ch_name = ch_name;
	}
	public String getCh_addr() {
		return ch_addr;
	}
	public void setCh_addr(String ch_addr) {
		this.ch_addr = ch_addr;
	}
	public String getCh_telno() {
		return ch_telno;
	}
	public void setCh_telno(String ch_telno) {
		this.ch_telno = ch_telno;
	}
	
	
	
}
